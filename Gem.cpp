#include "Gem.h"
#include<cstdlib>

/***************************************************************************
  功    能：无参构造函数，随机生成宝石
  输入参数：
  返 回 值：
  说    明：
***************************************************************************/
Gem::Gem()
{
	x = rand() % BOARD_SIZE;
	y = rand() % BOARD_SIZE;
	v = rand() % COLORS;
	score = 2;
}

/***************************************************************************
  功    能：构造函数
  输入参数：
  返 回 值：
  说    明：
***************************************************************************/
Gem::Gem(int x, int y, int v): x(x), y(y), v(v)
{
	score = 2;
}

/***************************************************************************
  功    能：重载比较等号
  输入参数：
  返 回 值：
  说    明：
***************************************************************************/
bool Gem::operator==(const Gem& b) const
{
	return v == b.v;
}
